# Operators (+, -, *, /, %)
print(2+8)   # Addition
print(9-3)  # Substraction
print(3*6)  # Multiplication
print(2**3) # Exponent  2 power of 3 = 8
print(8/3)  # Division with float output
print(8//3) # Division with int output
print(8%3)  # Remainder

# PEMDAS is the prioity of operation  () > ** > * or / > + or -
print(3 * 3 + 3 / 3 - 3)
print(3 * (3 + 3) / 3 - 3)